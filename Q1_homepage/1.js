var updates_form;

window.onload = function()
{
	updates_form = document.getElementById("updates_form");
	if( updates_form )
	setupupdates();
}
    var updatetable, totalupdates, abc;

function setupupdates()
{
	totalupdates = parseInt(window.localStorage.getItem("madhav:totalupdates"));
	if( !totalupdates )
		totalupdates = 0;
	updatetable = document.getElementById("newupdates");
	var row, cell;
	for(var i=1; i<=totalupdates; i++)
	{
		abc = window.localStorage.getItem("madhav:" + i);
		abc = JSON.parse(abc);
		row = updatetable.insertRow();
		cell = row.insertCell();
		cell.innerHTML = abc.name;
		cell = row.insertCell();
		cell.innerHTML = abc.updates;
	}
}

function updatesubmit()
{
	var abc = {
	name: document.forms["updates_form"]["name"].value,
	updates: document.forms["updates_form"]["updates"].value
	}
	window.localStorage.setItem("madhav:totalupdates", totalupdates + 1);
	window.localStorage.setItem("madhav:" + (totalupdates+1), JSON.stringify(abc));
}

